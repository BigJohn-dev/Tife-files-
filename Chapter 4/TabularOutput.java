public class TabularOutput {
	public static void main(String[] args) {

System.out.print("N\tN2\tN3\tN4");
	for(int count = 1; count <= 4; count++) {
		for(int value = 1; value <= 4; value++){
		System.out.print("\t" + count * value + " ");
}
	System.out.println();
}










}

}
